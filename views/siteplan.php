    <h1>My Dream Diaries</h1>
    <h2>Site Purpose</h2>
    <p>This website is for an author who wants to promote his first novel.  It will display episodic stories that help build the 
       world his novel takes place in.  The site is trying to promote interest about the novel prior to publication.</p>
    <h2>Target Audience</h2>
    <h3>Persona: Teenage Girl</h3>
        <img src="/images/girlpersona.jpg" alt="picture of Mary reading a book" width="300" height="200">
    <p>Mary Jones is a 18-year-old, middle class, high school student.  She is spends most of her time in school and is active in after-school sports.  
        She is frequently on her smart phone playing games and reading.  She also enjoys reading hard copy books.  When she gets home she 
        plays on her tablet or on her parent's computer.  She enjoys reading Young Adult fantasy books because it gives 
        her an escape from her everyday rituals.</p>
        <h3>Persona: Teenage Boy</h3>
        <img src="/images/boypersona.jpg" alt="picture of Joe reading a book" width="300" height="200">
        <p>Joe Thomas is a 16-year-old high school student.  He spends most of his time at school and at his after-school job where he typically works 
        until the evening. On his days off he goes to the library to use their computers or just check-out books, either audio or hard copy.  He stays
        up most nights reading or listening to the books he picked up at the library.  He enjoys reading Young Adult action adventure books because it 
        let's him escape from school and work drama.</p>
